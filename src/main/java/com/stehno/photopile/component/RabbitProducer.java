package com.stehno.photopile.component;

import org.springframework.amqp.rabbit.core.RabbitTemplate;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Component;

@Component
public class RabbitProducer {

    @Autowired
    private RabbitTemplate rabbitTemplate;

    public void publish( final String data ){
        final String routingKey = "events.import.completed";

        System.out.println("Publishing: " + data);

        rabbitTemplate.convertAndSend( routingKey, data );
    }
}
